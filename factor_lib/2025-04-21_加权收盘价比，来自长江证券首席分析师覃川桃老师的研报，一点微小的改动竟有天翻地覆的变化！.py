# (作者注) 根据这个说明来看，这个因子应该对过去21个交易日分钟级别的数据来计算。但是，笔者的代码是每天单独计算的，在评价的时候采取了三种不同的方法，第一种，过去21个交易日求均值；第二种，过去21个交易日求标准差；第三种，直接不做任何处理。
#
# (作者注) 在三种方法都尝试了一下之后，发现标准差的效果看起来好一点点。所以，就展示了一下标准差的评价结果。只不过，用标准差的话似乎与这个因子设计的初衷不相符了。
#
# (作者注) 标准差越大，证明在过去21个交易日中，标的有的时候在高位成交，有的时候不是，而且这两种状态出现的情况相当。标准差越低的话就意味着，标的大部分时间都在高位成交，或者大部分时间都不在高位成交。
#
# (作者注) 这个因子通过测试是与未来收益负相关的。理论上，如果标准差低，然后如果都是高位成交的话，按照研报中的说明，过去21天该标的可能都是在高位成交的，那么它未来收益率应该会下降。这似乎可以有一个修正的思路。本文我们暂且按下不表，等笔者改进一下试一试。（文末有一个简单的改进，效果还可以！）
