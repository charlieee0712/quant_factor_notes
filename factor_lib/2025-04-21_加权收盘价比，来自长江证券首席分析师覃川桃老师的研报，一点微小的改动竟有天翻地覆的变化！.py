#
# 同时，标准差低的时候，可能有两种情况，一种是均值高，一种是均值低。构造的目标就是将这两种情况区分出来。于是，尝试了一下标准差/均值和均值/标准差的情况，发现还真的可以。
#
# 其中，标准差/均值叫做变异系数，这个和直接用标准差区别不大。区别大的是它的倒数，也就是均值除以标准差。
