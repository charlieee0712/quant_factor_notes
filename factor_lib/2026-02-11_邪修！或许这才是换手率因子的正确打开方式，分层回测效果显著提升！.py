# **换手率**
#
# 在大A有一个传奇的因子，它叫做换手率。
#
# 笔者第一次见到这个因子，是在石川老师的《因子投资》一书中（最近好像出新版了），第二次见到这个因子是在复现Barra CNE6模型的时候。
#
# 这个因子的IC绝对值挺高的，但是其在分层回测上的表现就比较一般了。
#
# 于是，笔者一直在思考，对于这种IC很高的因子，如何能够提升其分层回测的表现呢？
#
# 在去年11月份的时候，笔者复现了中信建投陈升锐老师的一篇研报[基于日常收益的注意力溢出，一个IC和分层回测俱佳的因子！](https://mp.weixin.qq.com/s?__biz=MzkwNjYzMTEyMg==&mid=2247490377&idx=1&sn=9278ded584d008824cb74e206f2a7cfd&scene=21#wechat_redirect)，然后发现用行业和市值一起计算的注意力溢出，与注意力相比分层回测的结果大幅提升了（没有用行业市值计算的因子在[用行业和市值计算的注意力溢出，相比注意力的提升居然在这里！](https://mp.weixin.qq.com/s?__biz=MzkwNjYzMTEyMg==&mid=2247490403&idx=1&sn=9d6ec36fa55f57c474f5f636c562b920&scene=21#wechat_redirect)这篇文章中有介绍）。
#
# 因此，本文想在这一思想的基础上优化一下换手率因子，看看分层回测的效果是不是也会有提升。
#
# **计算思想**
#
# 这个因子的计算代码没有什么特别的了，就是将[基于日常收益的注意力溢出，一个IC和分层回测俱佳的因子！](https://mp.weixin.qq.com/s?__biz=MzkwNjYzMTEyMg==&mid=2247490377&idx=1&sn=9278ded584d008824cb74e206f2a7cfd&scene=21#wechat_redirect)这篇文章代码中attn这个属性直接赋值为换手率即可，所以不赘述了。
