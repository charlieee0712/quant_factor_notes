# 这个因子来自光大证券2017年的研报《基于K线最短路径构造的非流动性因子》。
#
# 首先，不考虑日内价格变动方向，直接进行无向叠加，求得股价变动的最短路径。
#
# 具体公式为：2\*（high-low）-abs（close-open)。
#
# 然后用上述计算得到的最短路径/成交额。
#
# 最后对每日计算得到的K线的非流动性求和，然后对过去21天取均值即可。
#
# 这个因子的逻辑是通过使用更高频的K线数据提升K线最短路径对股票交易时市场冲击的代理精度，大大提升了K线最短路径非流动性因子的有效性与预测能力。同时随着数据频率的提高，K线最短路径定义对经典定义下的非流动性因子提升程度愈加显著。比起经典的定义防线，K线最短路径能充分地利用高频数据新引入的信息。
