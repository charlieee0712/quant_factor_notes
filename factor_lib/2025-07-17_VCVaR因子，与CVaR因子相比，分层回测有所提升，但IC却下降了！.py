#
# 这个因子在IC上的表现还是不错的，但是在分层回测方面却存在着一些缺陷，单调性并不是太好，更不用提相交的情况了。
#
# 在同一篇研报当中，还提到了另外一个因子，叫做VCVaR。
#
# 这个因子和CVaR相比，就是用VWAR对尾部效应进行了强化。
#
# VWAR指的是成交量加权平均收益率，英文全称为Volume Weighted Average Return。对于日频VWAR来说，就是每分钟收益率按照成交量加权求和即可。对于分钟频的话，研报中给出的是分钟收益率乘以分钟成交量。
#
# 但是，笔者觉得这样做存在一个问题。因为，不同的股票成交量是不一样的，这里对于成交量大的股票来说，计算出来的因子值大概率是小于成交量小的股票的。这样的话，因子值小的就是成交量大的股票，因子值大的大概率就是成交量小的股票了，这就成为了一个和成交量相关性很高的因子了。
#
# 所以，笔者进行了一点点的改进，那就是用分钟收益率和分钟成交量占比相乘。
#
# 接下来，看看研报的作者，也就是严佳炜老师，为什么会做一个这样的改进。
#
# 在分钟高频数据环境下，某一分钟时段内的价格波动幅度及方向由买卖双方的强弱对比所决定，激烈成交的时段往往比稀疏成交的时段更具价格发现功能。
#
# 在流动性不足的情况下，可能会因为委买和委卖之间的差价过大而收集到大量的收益率数据噪音。
#
# 为了更准确地分析尾部特征，必须降低这些数据噪音，并强化有效数据。
#
# 将收益率换成VWAR之后，可以减少噪音干扰，突出尾部信息。研报中表示，VAWR数据比收益率数据具有更高的峰度和偏度，也就是说VWAR数据有更强的非正态性。
#
# 在因子评价的过程中，笔者发现VCVaR这个因子与CVaR这个因子相比，IC绝对值有所下降，但是分层回测的单调性有所提升，不过在净值曲线的相交问题上，并没有太大的改变。
