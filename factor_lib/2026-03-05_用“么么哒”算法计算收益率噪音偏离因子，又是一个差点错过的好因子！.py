# ============================================================
# 第一步，对收益率进行标准化。减去均值，可以认为得到了误差；然后除以误差的标准差，则是将误差的分布转换为标准正态分布。
#
# 第二步，衡量误差和标准正态分布之间的差异。这里将是计算这个因子的关键。
#
# 首先，如何获得标准正态分布的数据。这里，笔者提供两种方法。
#
# 第一种，随机生成一个标准正态分布的序列。
#
# 第二种，对过去21的交易日的分钟收益率序列标准化。因为，这个数据的观测越多，其误差也就越接近标准正态分布。
#
# 其次，如何衡量两个序列之间的分布差异。这里，将有多种方法。
#
# 第一种，研报中采用的，引入核密度函数估计收益率分布的pdf，再计算估计出的pdf与标准正态分布pdf的距离（差值的平方和）。由于研报中没写怎么引入核密度函数估计pdf的，所以笔者无从下手。直接点对点计算距离，显然是不科学的，因为每个点之间是独立同分布的。
#
# 第二种，直接计算序列的偏度。
#
# 第三种，统计学方法，KL散度之类的，但是这里笔者用的是最大平均偏差（MMD）。因为，根据笔者读研时的经验，这种方法效果较好。
#
# 需要注意的是，计算MMD的时候，两个序列长度是可以不一样的。换句话说，我们可以计算当前交易日标准化后的收益序列和过去21个交易日标准化后收益序列的MMD。但是，这样做的计算时间会大幅拉长，拉长110+倍。
