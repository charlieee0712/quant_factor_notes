# 除了上面提到的，还有跳跃波动率。这一系列至少有13个因子，包括已实现跳跃波动、已实现跳跃波动不对称、已实现上行跳跃波动、已实现下行跳跃波动、上下行跳跃波动不对称、已实现大程跳跃波动、已实现小程跳跃波动、已实现上行大程跳跃波动、已实现下行大程跳跃波动、上下行大程跳跃波动不对称、已实现上行小程跳跃波动、已实现下行跳跃波动和上下行小程跳跃波动不对称。
#
# 同样，这里也可以用残差收益率来计算，计算残差跳跃波动。还有一点，就是这里面的各种波动率还可以衍生出占比这一因子，又能多上不少！
