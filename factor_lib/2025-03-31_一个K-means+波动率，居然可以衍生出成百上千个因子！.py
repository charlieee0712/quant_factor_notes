# 众所周知，有很多与波动率有关的因子。
#
# 最常见的是这六个。
#
# 波动率，上行波动率，下行波动率、上行波动率占比、下行波动率占比和上下行波动率的不对称性。
#
# 前三个好理解，就是波动率，上涨时候的波动率，下跌时候的波动率。
#
# 那么后面三个呢？
#
# 上行波动率占比=上行波动率➗波动率
#
# 下行波动率占比=下行波动率➗波动率
#
# 上下行波动率的不对称性=（上行波动率-下行波动率）➗波动率
#
# 这里的波动率还是用收益率计算的，就有六个因子了。
#
# 同时，还可以将收益率换成残差收益率来计算，一个残差收益率对应六个因子的话，那就能有无数多的因子了。毕竟，稍微修改一下回归方差就能得到不同的残差收益率。
