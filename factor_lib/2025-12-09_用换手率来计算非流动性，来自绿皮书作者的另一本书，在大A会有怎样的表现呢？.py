# **非流动性**
#
# 但是，在券商研报中，使用的非流动性因子大都使用成交额计算的，即收益率的绝对值除以成交额。
#
# 而在量化绿皮书的作者另一本书《Active Equity Management》中，介绍了另外两种计算方式，第一种是收益率的绝对值除以成交额（该比率衡量的是每单位成交量引起的平均价格变动百分比），另一种则是收益率的绝对值除以换手率（这种方式能降低和市值的相关性）。
#
# 因子值越大非流动性越大，标的未来收益率会越高。因为，在同样的风险调整后收益下，投资者更愿意投资流动性高的标的。所以，流动性低的标的要有更高流动性溢价。
