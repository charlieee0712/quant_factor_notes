# ## 因子评价
# 先说明一下，uniform就是成交额分桶熵，之前计算的时候忘记改名字了。然后，amihud是非流动性分桶熵，diff则是用amihud-uniform得到的。
#
# 从相关性来看，diff因子和amihud因子的相关性还是挺高的。但是，两者用市值加权的分层回测结果的差距却很大。
#
# 关于diff因子，采用的是过去21个交易日的均值来进行低频化，因为用标准差低频化的话那个结果太辣眼睛了。
#
# ## IC分析
# 这个采用了黑科技之后的因子，其在IC上的表现好于成交量分桶熵，略差于非流动性分桶熵。
#
# 成交量分桶熵的IC累加刚刚到-4，而这个因子是超过了-4的，至于非流动性分桶熵则是接近-5了。
#
# 从年度的表现来看，成交量分桶熵因子的IC绝对值超过0.06的年份只有一年，而分桶熵差这个因子IC绝对值超过0。06的年份又三年。至于非流动性分桶熵，这个因子又两年的IC绝对值超过了0.07。
#
# ## 收益分析
# 在分层回测上，无论是和成交量分桶熵相比，还是和非流动性分桶熵相比，这个提升都是挺明显的。
