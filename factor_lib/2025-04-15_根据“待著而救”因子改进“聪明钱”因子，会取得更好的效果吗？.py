# (作者注) 第3-5行，获取成交量最大的时刻对应的索引。
#
# (作者注) 第6-10行，如果两个优势时刻相差不足五分钟，保留前一个，认为后一个还是前一个的跟随时刻。
#
# (作者注) 后面就是将不同时刻的成交额成交量累加到一起，然后计算不同时刻的vwap。
